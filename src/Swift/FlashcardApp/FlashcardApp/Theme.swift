// Theme.swift
import SwiftUI

extension Color {
    // アプリのメインの背景色
    static let appBackground = Color(UIColor.systemGroupedBackground)
    // カードやリストの行の背景色
    static let elementBackground = Color(UIColor.secondarySystemGroupedBackground)
    // アクセントカラー（ボタンやアイコン用）
    static let accent = Color.blue
}
